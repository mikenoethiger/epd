package ch.mikenoethiger.epd.model.http;

public class Pq {

	private int p;
	private int q;

	public Pq() {
	}
	
	public Pq(int p, int q) {
		super();
		this.p = p;
		this.q = q;
	}

	public int getP() {
		return p;
	}

	public void setP(int p) {
		this.p = p;
	}

	public int getQ() {
		return q;
	}

	public void setQ(int q) {
		this.q = q;
	}
	
	
}
